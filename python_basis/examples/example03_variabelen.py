# Introductie tot variabelen en types

print("Welkom bij: variabelen en types!")

# Een variabele is een naam voor een stukje data in het geheugen
naam = "Samantha"
leeftijd = 30
lengte = 1.75
student = True

print("Naam:", naam)
print("Leeftijd:", leeftijd)
print("Lengte:", lengte)
print("Student:", student)

print()
print("Python weet zelf welk type bij de waarde hoort.")
print("Je kunt het type controleren met type():")
print("Type van naam:", type(naam))
print("Type van leeftijd:", type(leeftijd))
print("Type van lengte:", type(lengte))
print("Type van student:", type(student))

print()
print("Je kunt ook rekenen met variabelen:")
x = 10
y = 3
som = x + y
print("x =", x, ", y =", y)
print("x + y =", som)
print("x ** y (macht) =", x ** y)
print("x / y (delen) =", x / y)
print("x // y (gehele deling) =", x // y)
print("x % y (restwaarde) =", x % y)

print()
print("Klaar! Je hebt geleerd hoe je variabelen maakt en types bekijkt.")
